                            )
                        ]
                    ),
                    instructions: [
                        new AssignInstructionTokenGroup(
                            subject: new ChainTokenGroup(chain: [new ThisRefTokenGroup(), 'mock']),
                            value: new VariableTokenGroup('mock'),
                        ),
                    ],
                ),
                new MethodTokenGroup(
                    name: 'getMockedClasses',
                    returnType: 'array',
                    static: true,
                    instructions: [
                        new ReturnInstructionTokenGroup(
                            new ArrayTokenGroup([new ClassReferenceTokenGroup('HookBasketAdapter')])
                        ),
                    ],
                ),
                new MethodTokenGroup(
                    name: 'getService',
                    returnType: new MultiTypeTokenGroup(
                        types: [
                            'internal\Baskets\Adapters\HookBasketAdapter',
